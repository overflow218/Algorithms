#include <iostream>
using namespace std;

int arr[16][16] = {0};
int n, ans = 0;

void find(int x, int y, int type)
{
    if(x == n || y == n || arr[x][y]) return;
    // 대각선은 추가체크 필요
    if(type == 2)
    {
        if((x > 0 && arr[x - 1][y]) || (y > 0 && arr[x][y - 1])) return;
    }

    if(x == n - 1 && y == n - 1)
    {
        ans++;
        return;
    }

    // 가로아닐때
    if(type != 0)
    {
        find(x + 1, y, 1);
    } 
    // 세로 아닐때
    if(type != 1)
    {
        find(x, y + 1, 0);
    }
    // 대각선은 공통
    find(x + 1, y + 1, 2);
}

int main()
{
    scanf("%d", &n);
    for(int i = 0; i < n; i++)
    {
        for(int j = 0; j < n; j++)
        {
            scanf("%d", &arr[i][j]);
        }
    }
    find(0, 1, 0);
    printf("%d\n", ans);
    return 0;
}

